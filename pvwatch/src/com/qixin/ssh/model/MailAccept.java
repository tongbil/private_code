package com.qixin.ssh.model;




public class MailAccept {
	private int id;
	private String emilName;
	private String emilPassword;
	private String server;
	private String port;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEmilName() {
		return emilName;
	}
	public void setEmilName(String emilName) {
		this.emilName = emilName;
	}
	public String getEmilPassword() {
		return emilPassword;
	}
	public void setEmilPassword(String emilPassword) {
		this.emilPassword = emilPassword;
	}
	public String getServer() {
		return server;
	}
	public void setServer(String server) {
		this.server = server;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	
	
	
}
